# Project 001: Welcome Message Generator

def main():
    name = input("What's your name? ")
    hobby = input("What's your hobby? ")

    print("\n--- Welcome Message Generator ---")
    print(f"Hello {name}, welcome to the Python world!")
    print(f"Great to hear you like {hobby}.")
    print("Let's build something great together!")

if __name__ == "__main__":
    main()
